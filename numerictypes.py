#Int
a = 13
b = 100
c = -66
print(a, b, c)
print(type(a))
#Float
x = 33.5
y = -25.8
z = 205.0
print(x, y, z)
print(type(x))
#Complex
d = 3 + 5j
print(d)
print(type(d))
#Binary
e = 0B1010
print(e, type(e))
#Hexadecimal
f = 0XFF
print(f, type(f))
#Octanumber
g = 0o512
print(g, type(g))
#Boolean
h = True
print(h, type(h))
print(8 > 9)

